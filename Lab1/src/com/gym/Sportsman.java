package com.gym;
import java.lang.*;


public class Sportsman {

    private int weight;
    private int height;
    private int age;
    private String name;


    public int getWeight() {
        return weight;
    }

    public int getHeight() {
        return height;
    }

    public int getAge() {
        return age;
    }

    public String getName() {
        return name;
    }

    public void setWeight(int newWeight) {
        weight = newWeight;
    }

    public void setHeight(int newHeight) {
        height = newHeight;
    }

    public void setAge(int newAge) {
        age = newAge;
    }

    public void setName(String newName) {
        name = newName;
    }
}
